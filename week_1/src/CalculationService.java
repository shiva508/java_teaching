public class CalculationService {
    public double additionOfTwoNumbers(double input1,double input2){
        double sum=input1+input2;
        return sum;
    }

    public double substractOfTwoNumbers(double input1,double input2){
        double substract=input1-input2;
        return substract;
    }

    public double multiplicationOfTwoNumbers(double input1,double input2){
        double multiplication=input1*input2;
        return multiplication;
    }

    public double divisionOfTwoNumbers(double input1,double input2){
        double divsion=input1%input2;
        return divsion;
    }
}
